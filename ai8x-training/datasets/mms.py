"""
MMS generated anomaly dataset.
"""
from pathlib import Path

import torch
import torch.nn.functional as F
from PIL import Image
from torch.utils.data import Dataset
from torchvision import transforms

import ai8x


class MMSAnomalyGrid(Dataset):
    """
    Static MMS image/mask pairs generated by data_augmentation.ipynb.

    Images are returned as normalized 3x128x128 tensors. Masks are returned as 32x32 integer
    class grids, where 0 is normal and 1 is anomaly.
    """

    image_size = 128
    grid_size = 32

    def __init__(self, root_dir, splits, transform=None, truncate=False):
        self.root_dir = Path(root_dir)
        self.transform = transform
        self.samples = []
        self.valid_indices = []

        if isinstance(splits, str):
            splits = (splits,)

        for split in splits:
            split_start = len(self.samples)
            image_dir = self.root_dir / split / 'images'
            mask_dir = self.root_dir / split / 'masks'

            if not image_dir.exists() or not mask_dir.exists():
                raise FileNotFoundError(
                    f'Expected generated MMS split at {image_dir} and {mask_dir}. '
                    'Run data_augmentation.ipynb first.'
                )

            for image_path in sorted(image_dir.glob('*.png')):
                mask_path = mask_dir / image_path.name
                if not mask_path.exists():
                    raise FileNotFoundError(f'Missing mask for {image_path}: {mask_path}')
                self.samples.append((image_path, mask_path))

            if split == 'val':
                self.valid_indices.extend(range(split_start, len(self.samples)))

        if truncate:
            self.samples = self.samples[:1]
            self.valid_indices = [i for i in self.valid_indices if i < len(self.samples)]

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, index):
        image_path, mask_path = self.samples[index]

        image = Image.open(image_path).convert('RGB')
        mask = Image.open(mask_path).convert('L')

        if self.transform is not None:
            image = self.transform(image)
        else:
            image = transforms.ToTensor()(image)

        mask_tensor = transforms.functional.pil_to_tensor(mask).float()
        mask_tensor = (mask_tensor > 0).float().unsqueeze(0)
        mask_tensor = F.max_pool2d(
            mask_tensor, kernel_size=self.image_size // self.grid_size,
            stride=self.image_size // self.grid_size
        )
        mask_tensor = mask_tensor.squeeze(0).squeeze(0).long()

        return image, mask_tensor


def _mms_get_datasets(data, dataset_name, load_train=True, load_test=True):
    """
    Load the generated MMS anomaly-grid dataset.
    """
    data_dir, args = data
    root_dir = Path(data_dir) / dataset_name

    transform = transforms.Compose([
        transforms.ToTensor(),
        ai8x.normalize(args=args),
    ])

    if load_train:
        train_dataset = MMSAnomalyGrid(root_dir, ('train', 'val'), transform=transform)
        print(f'Train+validation dataset length: {len(train_dataset)}')
        print(f'Validation samples: {len(train_dataset.valid_indices)}\n')
    else:
        train_dataset = None

    if load_test:
        test_dataset = MMSAnomalyGrid(
            root_dir, 'test', transform=transform, truncate=args.truncate_testset
        )
        print(f'Test dataset length: {len(test_dataset)}\n')
    else:
        test_dataset = None

    return train_dataset, test_dataset


def mms_get_datasets(data, load_train=True, load_test=True):
    """
    Load the original generated MMS anomaly-grid dataset.
    """
    return _mms_get_datasets(data, 'MMSGenerated', load_train, load_test)


def mms_v2_get_datasets(data, load_train=True, load_test=True):
    """
    Load the camera-domain augmented V2 generated MMS anomaly-grid dataset.
    """
    return _mms_get_datasets(data, 'MMSGeneratedV2', load_train, load_test)


def mms_v3_get_datasets(data, load_train=True, load_test=True):
    """
    Load the V3 generated MMS anomaly-grid dataset with good camera anchors.
    """
    return _mms_get_datasets(data, 'MMSGeneratedV3', load_train, load_test)


def mms_v4_get_datasets(data, load_train=True, load_test=True):
    """
    Load the V4 generated MMS anomaly-grid dataset with small object-contained masks.
    """
    return _mms_get_datasets(data, 'MMSGeneratedV4', load_train, load_test)


def mms_v5_get_datasets(data, load_train=True, load_test=True):
    """
    Load the V5 generated MMS anomaly-grid dataset with realistic object-contained masks.
    """
    return _mms_get_datasets(data, 'MMSGeneratedV5', load_train, load_test)


def mms_camera_test_get_dataset(data):
    """
    Load the held-out MAX78000 camera-domain test set.
    """
    data_dir, args = data
    root_dir = Path(data_dir) / 'MMSCameraTest'

    transform = transforms.Compose([
        transforms.ToTensor(),
        ai8x.normalize(args=args),
    ])

    return MMSAnomalyGrid(root_dir, 'test', transform=transform,
                          truncate=args.truncate_testset)


